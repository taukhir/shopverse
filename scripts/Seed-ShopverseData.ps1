    }

    Wait-Job -Job $jobs -Timeout $BatchTimeoutSeconds | Out-Null
    $timedOut = @($jobs | Where-Object State -eq "Running")
    if ($timedOut) {
        $timedOut | Stop-Job
    }
    for ($jobIndex = 0; $jobIndex -lt $jobs.Count; $jobIndex++) {
        $job = $jobs[$jobIndex]
        $result = Receive-Job -Job $job -ErrorAction SilentlyContinue
        if ($null -eq $result) {
            $entry = $batch[$jobIndex]
            $result = [pscustomobject]@{
                Success = $false
                OrderIndex = $entry.OrderIndex
                Username = $entry.Customer.Username
                ProductId = $entry.Product.ProductId
                OrderId = $null
                OrderNumber = $null
                CorrelationId = "api-seed-correlation-{0:D4}" -f $entry.OrderIndex
                IdempotencyKey = "api-seed-checkout-{0:D4}" -f $entry.OrderIndex
                Error = "Checkout batch exceeded the ${BatchTimeoutSeconds}s timeout"
            }
        }
        $orderResults.Add($result)
        Remove-Job -Job $job -Force
    }

    $completedCount = [Math]::Min($offset + $batch.Count, $checkoutPlan.Count)
    Write-Host "Checkout progress: $completedCount / $($checkoutPlan.Count)"
}

$manifestDirectory = Split-Path -Parent $ManifestPath
if (-not [string]::IsNullOrWhiteSpace($manifestDirectory)) {
    New-Item -ItemType Directory -Force -Path $manifestDirectory | Out-Null
}
$manifest = [pscustomobject]@{
    generatedAtUtc = [DateTime]::UtcNow.ToString("o")
    gatewayUrl = $GatewayUrl
    requested = @{ customers = $CustomerCount; products = $ProductCount; orders = $OrderCount }
    summary = @{
        customersCreated = @($userResults | Where-Object Status -eq "created").Count
        customersExisting = @($userResults | Where-Object Status -eq "existing").Count
        productsUpserted = $productResults.Count
        checkoutSucceeded = @($orderResults | Where-Object Success).Count
        checkoutFailed = @($orderResults | Where-Object { -not $_.Success }).Count
    }
    customers = $userResults
    products = $productResults
    orders = $orderResults
}
$manifest | ConvertTo-Json -Depth 8 | Set-Content -LiteralPath $ManifestPath -Encoding utf8
Update-GeneratedCredentials -Customers $customers

$failedOrders = @($orderResults | Where-Object { -not $_.Success })
$manifest.summary | Format-List
Write-Host "Credentials: $CredentialsPath"
Write-Host "Manifest: $ManifestPath"

if ($failedOrders.Count -gt 0) {
    $failedOrders | Select-Object OrderIndex, Username, ProductId, Error | Format-Table -AutoSize
    throw "$($failedOrders.Count) checkout requests failed. Re-run the script with the same defaults; successful orders reuse their idempotency keys."
}
