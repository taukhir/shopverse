$checkoutWorker = {
    param($WorkerGatewayUrl, $Username, $Password, $ProductId, $OrderIndex, $TimeoutSeconds)

    $idempotencyKey = "api-seed-checkout-{0:D4}" -f $OrderIndex
    $correlationId = "api-seed-correlation-{0:D4}" -f $OrderIndex
        $body = @{
