Write-Host "Created or confirmed $($userResults.Count) customers and upserted $($productResults.Count) products. Starting $OrderCount checkout requests in batches of $BatchSize..."

$checkoutWorker = {
    param($WorkerGatewayUrl, $Username, $Password, $ProductId, $OrderIndex, $TimeoutSeconds)

    $idempotencyKey = "api-seed-checkout-{0:D4}" -f $OrderIndex
    $correlationId = "api-seed-correlation-{0:D4}" -f $OrderIndex
    $body = @{ items = @(@{ productId = $ProductId; quantity = 1 }) } | ConvertTo-Json -Depth 6 -Compress

    for ($attempt = 1; $attempt -le 4; $attempt++) {
        try {
            $login = Invoke-RestMethod -Method Post -Uri "$WorkerGatewayUrl/auth/login" -ContentType "application/json" -Body (@{ username = $Username; password = $Password } | ConvertTo-Json -Compress) -TimeoutSec $TimeoutSeconds -ErrorAction Stop
            if ([string]::IsNullOrWhiteSpace($login.token)) {
                throw "Login returned no token"
            }
            $headers = @{
                Authorization = "Bearer $($login.token)"
                "Idempotency-Key" = $idempotencyKey
                "X-Correlation-Id" = $correlationId
            }
            $order = Invoke-RestMethod -Method Post -Uri "$WorkerGatewayUrl/api/v1/orders/checkout" -Headers $headers -ContentType "application/json" -Body $body -TimeoutSec $TimeoutSeconds -ErrorAction Stop
            return [pscustomobject]@{
                Success = $true
                OrderIndex = $OrderIndex
                Username = $Username
                ProductId = $ProductId
                OrderId = $order.id
                OrderNumber = $order.orderNumber
                CorrelationId = $correlationId
                IdempotencyKey = $idempotencyKey
                Error = $null
            }
        } catch {
            $statusCode = if ($null -eq $_.Exception.Response) { 0 } else { [int]$_.Exception.Response.StatusCode }
            if ($attempt -eq 4 -or $statusCode -notin @(409, 429, 502, 503, 504)) {
                return [pscustomobject]@{
                    Success = $false
                    OrderIndex = $OrderIndex
                    Username = $Username
                    ProductId = $ProductId
                    OrderId = $null
                    OrderNumber = $null
                    CorrelationId = $correlationId
                    IdempotencyKey = $idempotencyKey
                    Error = "HTTP ${statusCode}: $($_.Exception.Message)"
                }
            }
            Start-Sleep -Milliseconds (350 * $attempt)
        }
    }
}

