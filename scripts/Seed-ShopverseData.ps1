function Get-HttpStatusCode {
    param([System.Management.Automation.ErrorRecord]$ErrorRecord)

    if ($null -eq $ErrorRecord.Exception.Response) {
        return 0
    }

    return [int]$ErrorRecord.Exception.Response.StatusCode
}

function Invoke-ShopverseJson {
    param(
        [ValidateSet("Get", "Post", "Put")]
        [string]$Method,
        [string]$Uri,
        [hashtable]$Headers,
        [object]$Body,
        [int]$TimeoutSeconds,
        [int[]]$RetryStatusCodes = @(429, 502, 503, 504)
    )

    $jsonBody = if ($null -eq $Body) { $null } else { $Body | ConvertTo-Json -Depth 8 -Compress }

    for ($attempt = 1; $attempt -le 4; $attempt++) {
        try {
            $parameters = @{
                Method = $Method
                Uri = $Uri
                Headers = $Headers
                TimeoutSec = $TimeoutSeconds
                ErrorAction = "Stop"
            }
            if ($null -ne $jsonBody) {
                $parameters.ContentType = "application/json"
                $parameters.Body = $jsonBody
            }
            return Invoke-RestMethod @parameters
        } catch {
            $statusCode = Get-HttpStatusCode $_
            if ($attempt -eq 4 -or $statusCode -notin $RetryStatusCodes) {
                throw
            }
            Start-Sleep -Milliseconds (250 * $attempt)
        }
    }
}

function Get-AccessToken {
    param(
        [string]$Username,
        [string]$Password
    )

    $login = Invoke-ShopverseJson `
        -Method Post `
        -Uri "$GatewayUrl/auth/login" `
        -Headers @{} `
        -Body @{ username = $Username; password = $Password } `
        -TimeoutSeconds $RequestTimeoutSeconds

    if ([string]::IsNullOrWhiteSpace($login.token)) {
        throw "Login for '$Username' did not return an access token."
    }

    return $login.token
}

function Update-GeneratedCredentials {
    param(
        [object[]]$Customers
    )

    $directory = Split-Path -Parent $CredentialsPath
    if (-not [string]::IsNullOrWhiteSpace($directory)) {
        New-Item -ItemType Directory -Force -Path $directory | Out-Null
    }

    $startMarker = "<!-- SHOPVERSE-API-SEED-START -->"
    $endMarker = "<!-- SHOPVERSE-API-SEED-END -->"
    $rows = $Customers | ForEach-Object {
        "| $($_.Username) | $($_.FirstName) $($_.LastName) | $CustomerPassword | ROLE_CUSTOMER |"
    }
    $section = @(
        $startMarker,
        "",
        "## Generated API Seed Accounts",
        "",
        "Generated by scripts/Seed-ShopverseData.ps1 for local POC use only.",
        "",
        "| Username | Name | Password | Role |",
        "| --- | --- | --- | --- |",
        $rows,
        "",
        "Administrator used by this run: `$AdminUsername`.",
        $endMarker
    ) -join [Environment]::NewLine

    $current = if (Test-Path -LiteralPath $CredentialsPath) {
        Get-Content -LiteralPath $CredentialsPath -Raw
    } else {
        "# Shopverse Local Demo Credentials$([Environment]::NewLine)$([Environment]::NewLine)Keep this ignored file on your local machine only.$([Environment]::NewLine)"
    }

    $pattern = "(?s)" + [regex]::Escape($startMarker) + ".*?" + [regex]::Escape($endMarker)
    $updated = if ($current -match $pattern) {
        [regex]::Replace($current, $pattern, [System.Text.RegularExpressions.MatchEvaluator]{ param($match) $section })
    } else {
        $current.TrimEnd() + [Environment]::NewLine + [Environment]::NewLine + $section + [Environment]::NewLine
    }

    Set-Content -LiteralPath $CredentialsPath -Value $updated -Encoding utf8
}

